	<div class="col-sm-12" style="border:3px solid DodgerBlue;font-size:16px;">
	<p align="justify" style="margin-left:15px;margin-right:15px;margin-top:15px;font-family:tahoma;">The seeds of the first Homoeopathic Medical College of Lucknow were sown in 1912 by Dr. G.N. Ohdedar which existed till 1920 when Dr. B.S. Tandon of Lucknow did a herculean task by establishing National Homoeopathic Medical College and Hospital in 1921. 
	Later on it was registered under Society Registration Act 21 1860. The first Principal of this college was Dr. J.C. Das, who was the direct disciple of Dr. J.T. Kent of Chicago, USA.The first course started in N.H.M.C. was two year H.M.B which was later upgraded to three year from 1940 to 1949. In the year 1950, 
	a four year diploma course of B.M.S. was started which was later affiliated to Homoeopathic Medicine Board in 1954. It was provincial zed by Uttar Pradesh Government in the year 1968. The first five year degree course known as G.H.M.S. was commenced in 1958 and the college was affiliated to Agra University Agra. 
	N.H.M.C. became Asia’s first degree college of Homoeopathy in 1961.
	
	<p align="justify" style="margin-left:15px;margin-right:15px;font-family:tahoma;">The college was disaffiliated by Agra University Agra in 1966 due to nonconformity of minimum standards and again B.M.S. diploma course was enforced. It was in the year 1971 when the college was affiliated to Kanpur University Kanpur and G.H.M.S. 
	degree course was reinstated which continued till 1980.To give uniformity, the first B.H.M.S. five and half year degree course was started in 1981 for which admissions were taken through C.P.M.T. At that point of time the college was again affiliated to erstwhile Agra University Agra which was later named as Bhim Rao Ambedkar University Agra.
	The admission capacity to this college is 50. The college continued in the same old rented building situated in Cant Road kaiserbag, Lucknow.</p>
			
	<p align="justify" style="margin-left:15px;margin-right:15px;font-family:tahoma;">It was historical event when the Govt. of Uttar Pradesh sanctioned Rs. 1611.31 lakh for purchasing five acre land in Gomti Nagar and for construction of new building to house N.H.M.C.
	The new premises was inaugurated by no less than the then Prime Minister of India, Sri Atal Bihari Bajpai on 27th Feb 2000. The present campus has five storied educational cum Administrative block, two storied OPD block, four storied girls’ and boys’ hostel and quarter for teaching and non-teaching staff.
	Another landmark achievement is the installation of our Master Dr. HAHNEMANN 12 feet bronze statue with 10 feet elevation total 22 feet on the main crossing near State National Homoeopathic Medical College named as Hahnemann Chauraha.</p>
	
	<p align="justify" style="margin-left:15px;margin-right:15px;font-family:tahoma;">The seeds of this institution were sown by Dr. B. S. Tandon, famous Homoeopathic Physician of his time, on August 18, 1961 with the name Hahnemannian Homoeopathic Medical College and Hospital. The first batch had only eight students and the classes were being 
	conducted at his residence in Lalbagh because the college did not have a building of its own.
	Gradually with the swelling number of students the college was shifted to M. D. Shukla Inter College at Nadan Mahal Road, Lucknow.</p>
	
	<p align="justify" style="margin-left:15px;margin-right:15px;font-family:tahoma;">Presently the office of the Homoeopathic Medicine Board, U.P and State Drug Laboratory are housed in the old premises of State MHMC at 2, 
	Nabi Ullah Road, Lucknow.</p>
